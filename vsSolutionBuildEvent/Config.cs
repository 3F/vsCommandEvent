using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Xml.Serialization;
using System.IO;

namespace reg.ext.vsSolutionBuildEvent
{
    class Config
    {
        /// <summary>
        /// Current config version
        /// </summary>
        const string VERSION = "0.4";

        /// <summary>
        /// data format
        /// </summary>
        public static SolutionEvents data = null;

        /// <summary>
        /// configuration file in the file system
        /// </summary>
        private const string _name = ".xprojvsbe";

        /// <summary>
        /// path to file settings
        /// </summary>
        private static string _path = "";

        /// <summary>
        /// filename with full path
        /// </summary>
        private static string _FullName
        {
            get { return _path + _name; }
        }

        /// <summary>
        /// initialization of settings
        /// </summary>
        /// <param name="path">the path to the configuration file</param>
        public static void load(string path)
        {
            _path = path;
            try
            {
                using(FileStream stream = new FileStream(_FullName, FileMode.Open, FileAccess.Read, FileShare.Read))
                {
                    XmlSerializer xml   = new XmlSerializer(typeof(SolutionEvents));
                    data                = (SolutionEvents)xml.Deserialize(stream);
                    compatibilityCheck(stream);
                }
            }
            catch (Exception)
            {
                //Debug.Assert(false);
                data = new SolutionEvents();
            }
        }

        /// <summary>
        /// with changing a path
        /// </summary>
        /// <param name="path">the path to the configuration file</param>
        public static void save(string path)
        {
            _path = path;
            save();
        }

        public static void save()
        {
            using(TextWriter stream = new StreamWriter(_FullName))
            {
                if(data == null){
                    data = new SolutionEvents();
                }
                XmlSerializer xml = new XmlSerializer(typeof(SolutionEvents));
                xml.Serialize(stream, data);
            }
        }

        public static string getWorkPath()
        {
            return _path;
        }

        /// <summary>
        /// Check version and reorganize structure if needed..
        /// </summary>
        /// <param name="stream"></param>
        protected static void compatibilityCheck(FileStream stream)
        {
            Version ver = Version.Parse(data.settings.compatibility);

            if(ver.Major == 0 && ver.Minor < 4) {
                PaneVS.instance.show();
                try {
                    Upgrade.Migration03_04.migrate(stream);
                    //TODO: to ErrorList
                    PaneVS.instance.outputString("[info] Successfully upgraded configuration 0.3 -> 0.4\nPlease, save manually!\n\n");
                }
                catch(Exception e){
                    //TODO: to ErrorList
                    PaneVS.instance.outputString(String.Format("[warning] {0}\n{1}\n\n-----\n{2}\n\n", "cannot upgrade config 0.3 -> 0.4", "Please contact to author.", e.Message));
                }
            }
            
            //update version
            data.settings.compatibility = VERSION;
        }

        protected Config(){}
    }
}
